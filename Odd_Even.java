package JavaAssingment;



public class Odd_Even {
    public static void main(String[] args) {

        for(int i=1; i<=100;i++)
        {
            int a=i;
            if(a%2==0)
            {
                System.out.println(a + " : Even");
            }
            else
            {
                System.out.println(a + " : Odd");
            }
    }

    }
}
